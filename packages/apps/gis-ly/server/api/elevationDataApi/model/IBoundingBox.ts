export interface IBoundingBox {
  /**
   * North latitude.
   *
   * @type {number}
   * @memberof IBoundingBox
   */
  north: number,
  /**
   * South latitude.
   *
   * @type {number}
   * @memberof IBoundingBox
   */
  south: number,
  /**
   * West longitude.
   *
   * @type {number}
   * @memberof IBoundingBox
   */
  west: number,
  /**
   * East longitude.
   *
   * @type {number}
   * @memberof IBoundingBox
   */
  east: number,
}